mood = input("What is your mood today? ")

if mood == "happy":
    print("It's great to see you happy!")
elif mood == "nervous":
    print("Take a deep breath 3 times!")
elif mood == "sad":
    print("Cheer up, mate!")
elif mood == "excited":
    print("Slow down, mate!")
elif mood == "relaxed":
    print("Enjoy your self, mate!")
else:
    print("I don't recognize this mood")
